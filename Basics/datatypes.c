#include <stdio.h>
#include <stdbool.h>
//VARIABLES SYNTAX -> TYPE NAME = VALUE;

int main(){

  //INTEGERS
  int myNum = 15;
  printf("myNum: %d", myNum);

  //FLOAT
  float myFloatNum = 15.5;
  printf("myFloatNum: %f", myFloatNum);

  //DOUBLE
  double myDoubleNum = 100.15;
  printf("myDoubleNum: %lf");

  //CHARACTER
  char myChar = 'w';
  printf("myChar: %c", myChar);

  //BOOLEAN (NEED TO IMPORT STDBOOL.H)
  bool myBool = false;
  printf("myBool: %b", myBool);

  return 0;
}



